Display::Display(int __deviceID)
{
  // Define o ID do dispositivo.
  this->_deviceID = __deviceID;
}

bool Display::begin()
{
  // Instanciando a classe do display via I2C (SDA e SCL).
  if(_screen.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS) == false){
    return false;
  }

  // Definicoes padroes
  _screen.setTextColor(SSD1306_WHITE);
  _screen.clearDisplay();

  return true;
}

// Dados de identificacao do sensor.
void Display::showDeviceID()
{
  _screen.setTextSize(1);

  _screen.setCursor(79, 4);
  _screen.print("devID:");

  _screen.setCursor(114, 4);
  _screen.print(this->_deviceID);
}

// Exibe a tela de carregamento da pulseira.
void Display::showLoading()
{
  _screen.clearDisplay();
  _screen.drawBitmap(0, 0, image_border, 128, 64, 1);
  this->showDeviceID();

  _screen.setTextSize(2);
  _screen.setCursor(3, 2);
  _screen.print("SENSOR");

  _screen.setCursor(5, 30);
  _screen.print("Carregando");

  _screen.display();
}

// Exibe uma mensagem de erro na tela.
void Display::showErrorMessage(String __message)
{
  _screen.clearDisplay();
  _screen.drawBitmap(0, 0, image_border, 128, 64, 1);
  this->showDeviceID();

  // Cabecalho
  _screen.setTextSize(2);
  _screen.setCursor(3, 2);
  _screen.print("ALERTA");

  // Informacao
  _screen.setTextSize(1);
  _screen.setCursor(4, 25);
  _screen.print(__message);

  _screen.display();
}

// Exibe os dados recebidos do sensor na tela.
void Display::showDataSensor(int __spo2, int __bpm, float __temperature)
{
  _screen.clearDisplay();
  _screen.drawBitmap(0, 0, image_border, 128, 64, 1);
  this->showDeviceID();

  // Texto de tamanho 1:1 (Pequenos)
  _screen.setTextSize(1);

  _screen.setCursor(31, 26);
  _screen.print(F("%SpO2"));

  _screen.setCursor(50, 40);
  _screen.print(F("."));

  _screen.setCursor(55, 45);
  _screen.print(F("C"));

  _screen.setCursor(105, 27);
  _screen.print(F("bpm"));


  // Texto de tamanho 2:1 (Medios)
  _screen.setTextSize(2);

  _screen.setCursor(3, 2);
  _screen.print(" UNIP");

  _screen.setCursor(76, 20);
  _screen.print(F("PR"));

  // Valor Oxigenacao: 00 %SpO2
  _screen.setCursor(4, 21);
  _screen.print(int(__spo2)); 

  // Valor Temperatura: 00.0C
  _screen.setCursor(4, 45);
  _screen.print(double(__temperature), 1); 


  // Texto de tamanho 3:1 (Grandes)
  _screen.setTextSize(3);

  // Define a posicao do texto para 2 ou 3 caracteres.
  if(__bpm > 99){
    _screen.setCursor(73, 39);
  }
  else{
    _screen.setCursor(82, 39);
  }

  // Valor Batimentos: 000 PRbpm
  _screen.print(int(__bpm)); 

  _screen.display();
}
